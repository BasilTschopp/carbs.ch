        <p class='InfoTitle'>Sinn und Zweck</p>
        <p class='InfoText'>Eine Webapplikation zur Berechnung von Kohlenhydraten und weiteren Nährstoffen in den Mahlzeiten. Da das Abschätzen der Portionsgrößen anspruchsvoll sein kann, sind die gängigen Portionsgrößen hinterlegt. Zudem können Typ-1-Diabetiker den Faktor für die Kohlenhydrate angeben, um direkt die Insulindosis zu berechnen.</p>

        <p class='InfoTitle'>Quellenangaben</p>
        <p class='InfoText'>Die Angaben der Nährwerte pro 100g bzw. 100ml für die allgemeinen Lebensmittel stammen von der Naehrwertdaten.ch Webseite, produktspezifische Angaben von den Webseiten der Hersteller. Die Portionsgrösse stammen aus diversen, nicht urheberrechtlich geschützen Quellen.
        </p>

        <p class='InfoTitle'>Ausschluss</p>
        <p class='InfoText'>Alle Angaben wurden nach bestem Wissen und Gewissen erstellt, dennoch kann keine Garantie für die Richtigkeit und Vollständigkeit übernommen werden. Zudem können die Nährwerte je nach Zubereitung und die Portionsgrössen im Allgemeinen variieren.</p>

